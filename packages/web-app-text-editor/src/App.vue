<template>
  <main id="text-editor">
    <text-editor-app-bar ref="bar_comp" />
    <oc-notifications>
      <oc-notification-message
        v-if="lastError"
        :message="lastError"
        status="danger"
        @close="clearLastError"
      />
    </oc-notifications>
    <div class="uk-flex">
      <div class="uk-container uk-width-1-1">
        <oc-textarea
          id="text-editor-input"
          label=""
          name="input"
          full-width
          :value="currentContent"
          class="uk-height-1-1"
          :rows="20"
          @input="onType"
          :disabled="isReadOnly"
        />
      </div>
    </div>
  </main>
</template>
<script>
import TextEditorAppBar from './TextEditorAppBar.vue'
import { mapActions, mapGetters } from 'vuex'

export default {
  name: 'TextEditor',
  components: {
    TextEditorAppBar
  },
  computed: {
    ...mapGetters(['activeFile']),
    ...mapGetters('TextEditor', ['currentContent', 'lastError', 'isReadOnly'])
  },
  mounted() {
    console.log("DIOGO > STARTED")
    if (this.$route.params.filePath === '') {
      this.$router.push({
        path: '/files'
      })
      return
    }
    // FIXME this is always edit or create... need to find file permissions
    // if (this.$route.params.mode === 'edit' || 
    //   this.$route.params.mode === 'create') {
    //   // This file can be edited
    this.setWriteMode()
    // }
    this.loadFile({
      filePath: this.$route.params.filePath,
      client: this.$client,
      public: this.$route.name === 'text-editor-public'
    })
    this.$refs.bar_comp.registerKeyboardShortcuts()
  },
  methods: {
    ...mapActions('TextEditor', ['updateText', 'loadFile', 'setWriteMode', 'clearLastError', 'handleSKey']),
    onType(e) {
      this.updateText(e)
    }
  }
}
</script>
